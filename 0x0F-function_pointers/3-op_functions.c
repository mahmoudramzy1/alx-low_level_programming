#include "3-calc.h"
/**
 * op_add - adds two ints
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two ints
 * @a: the first int
 * @b: the second int
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two ints
 * @a: first int
 * @b: second int
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divids two ints
 * @a: first int
 * @b: second int
 * Return: the quotirnt
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo two ints
 * @a: first int
 * @b: second int
 * Return: the reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
